package springsample.set.property;

public class Motorcycle {
	
	private int wheelNo;

	public int getWheelNo() {
		return wheelNo;
	}

	public void setWheelNo(int wheelNo) {
		this.wheelNo = wheelNo;
	}

	@Override
	public String toString() {
		return "Motorcycle [wheelNo=" + wheelNo + "]";
	}
	
	



}
